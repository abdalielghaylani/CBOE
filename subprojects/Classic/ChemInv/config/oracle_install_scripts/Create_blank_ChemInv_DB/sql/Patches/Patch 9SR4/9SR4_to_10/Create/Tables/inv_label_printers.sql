	"LABEL_PRINTER_ID" NUMBER(9) NOT NULL,
	"SERVER_NAME" VARCHAR2(100) NOT NULL,
	"QUEUE_NAME" VARCHAR2(100) NOT NULL,
	"LPR_DEFINITION_ID_FK" NUMBER(9) NOT NULL,
	"REPORTTYPE_ID_FK" NUMBER(9) NOT NULL,
	"DISPLAY_NAME" VARCHAR2(200) NOT NULL,
	"IS_AVAILABLE" NUMBER(1,0) NOT NULL,
	CONSTRAINT "INV_LABEL_PRINTERS_PK"
		PRIMARY KEY("LABEL_PRINTER_ID") USING INDEX TABLESPACE &&indexTableSpaceName,
	CONSTRAINT "INV_LABEL_PRINTERS_U" 
		UNIQUE ("DISPLAY_NAME") USING INDEX TABLESPACE &&indexTableSpaceName,
