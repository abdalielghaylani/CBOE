CREATE SEQUENCE SEQ_INV_LABEL_PRINTERS_VALUES INCREMENT BY 1 START WITH 1000 MAXVALUE 1.0E28 MINVALUE 1 NOCYCLE;

CREATE OR REPLACE TRIGGER "TRG_INV_LABEL_PRINTERS_VALUES"
    BEFORE INSERT
    ON "INV_LABEL_PRINTERS"
    FOR EACH ROW
    begin
        if :new.LABEL_PRINTER_ID is null then
            select SEQ_INV_LABEL_PRINTERS_VALUES.nextval into :new.LABEL_PRINTER_ID from dual;
        end if;
    end;
